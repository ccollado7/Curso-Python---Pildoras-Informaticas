#Defino la funcion sumar
def sumar(op1,op2):
	print("El resultado de la suma es: ",(op1+op2))

#Defino la funcion restar
def restar(op1,op2):
	print("El resultado de la resta es: ",(op1-op2))

#Defino la funcion multiplicar
def multiplicar(op1,op2):
	print("El resultado de la multiplicacion es: ",(op1*op2))

#Defino la funcion dividir
def dividir(op1,op2):
	print("El resultado de la division es: ",(op1/op2))

#Defino la funcion potencia
def potencia(op1,op2):
	print("El resultado de la potenciacion es: ",(op1**op2))

#Defino la funcion redondear
def redondear(numero):
	print("El numero redondeado es: ",(round(numero)))